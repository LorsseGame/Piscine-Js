// Exercice 4: Aplatir un tableau de tableaux
var tab = [[1, 2, 3], [4, 5, 6], [7, 8, 9]];
// Créer une fonction flatten qui prend en paramètre un tableau de tableaux et retourne un tableau aplati.
function flatten(array) {
    return array.flat();
}

// Afficher le tableau aplati
console.log(flatten(tab)); 
